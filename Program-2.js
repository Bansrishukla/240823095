
// 2. Find the Maximum of Two Numbers
// Definition: Given two numbers, print the larger one.

// Function to find the maximum of two numbers
function findMax(num1, num2) {
    if (num1 > num2) {
        console.log(num1);
    } else {
        console.log(num2);
    }
}

// Example input
let num1 = 5;
let num2 = 9;

// Calling the function
findMax(num1, num2);